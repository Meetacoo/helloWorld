public class OtherVariables {
    public static void main(String[] args) {
        char a = 'A';
        boolean b = true;
        int c = 3;
        String d = "Hello World!";
        float e = 5f;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}